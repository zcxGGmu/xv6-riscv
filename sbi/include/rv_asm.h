#ifndef __RV_ASM_H__
#define __RV_ASM_H__

#ifndef __ASSEMBLER__

#define csr_read(csr)                                           \
	({                                                      \
		register unsigned long __v;                     \
		__asm__ __volatile__("csrr %0, " __ASM_STR(csr) \
				     : "=r"(__v)                \
				     :                          \
				     : "memory");               \
		__v;                                            \
	})

#endif /* !__ASSEMBLER__ */

#endif